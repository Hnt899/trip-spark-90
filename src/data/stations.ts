// Функция для получения станции по городу
export const getStation = (city: string, index: number = 0): string => {
  const stations = stationMap[city] || [city];
  return stations[index % stations.length];
};

// Получить все станции для города
export const getStationsForCity = (city: string): string[] => {
  return stationMap[city] || [city];
};

